public class Customer {
    /**
     * Member fields the Customer
     */
    private String name, surname;

    /**
     * Setter for the name of Customer
     * @param name initalize the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Getter for the name of the Customer
     * @return returns the name of the Customer
     */
    public String getName() {
        return name;
    }
    /**
     * Setter for the surname of Customer
     * @param surname initalize the surname
     */
    public void setSurname(String surname) {
        this.surname = surname;
    }

    /**
     * Getter for the surname of the Customer
     * @return returns the surname of the Customer
     */
    public String getSurname() {
        return surname;
    }

}
